window.V = function(item, nClass = "animate", person = null, number = "singular", tense = "infinitive perfective", options = {}) {
    // 1. SMART LOOKUP: If item is already an object, use it. Otherwise, search by ID or English.
    let wordObj = (item && typeof item === 'object') ? item : (lexicon.find(w => w.id === item) || findByEn(item));
    
    if (!wordObj || !wordObj.tv) {
        console.error("Verb not found in lexicon:", item);
        return "[Unknown Verb]";
    }

    // 2. Extract the stem
    const verbStem = wordObj.tv;

    // 3. Ensure we pass the right number of arguments to conjugate
    // Based on your earlier code, conjugate(stem, vClass, person, number, tense, options)
    // Note: If your engine needs 'isTransitive', put it inside the options object.
    const result = verbConjugator.conjugate(verbStem, nClass, person, number, tense, options);
    
    if (!result) return "[Error]";

    const form = result.toLowerCase();

    // 4. Store in lookup
    tvaaliLookup[form] = {
        en: wordObj.entries[0].senses[0].en[0],
        type: 'verb',
        data: wordObj,
        details: { person, number, tense, ...options }
    };

    return result;
};

