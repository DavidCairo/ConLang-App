const NUM = (val, type = "cardinal") => {
    if (val === 0) {
        const res = numbers[0][type];
        tvaaliLookup[res.toLowerCase()] = { type: 'number', value: 0, en: "0" };
        return res;
    }

    const buildValue = (n) => {
        if (n < 12) return numbers[n].cardinal;
        const divisor = Object.keys(numBases).map(Number).sort((a, b) => b - a).find(p => p <= n);
        const count = Math.floor(n / divisor);
        const remainder = n % divisor;
        let part = `${buildValue(count)}-${numBases[divisor]}`;
        if (remainder > 0) part += `-${buildValue(remainder)}`;
        return part;
    };

    const fullCardinal = buildValue(val);
    let finalString = fullCardinal;

    // Logic for ordinals
    if (type === "ordinal") {
        const parts = fullCardinal.split("-");
        const lastWord = parts.pop();
        let ordinalSuffix = lastWord + "ar";
        for (let k in numbers) {
            if (numbers[k].cardinal === lastWord) {
                ordinalSuffix = numbers[k].ordinal;
                break;
            }
        }
        finalString = [...parts, ordinalSuffix].join("-");
    }
    
    tvaaliLookup[finalString.toLowerCase()] = {
        type: 'number',
        value: val,
        en: val.toString()
    };

    return finalString;
};

// This creates a "Master Lookup" for every possible form
function generateMorphologyMap() {
    const lookup = {};

    // 1. Map Nouns
    lexicon.filter(w => w.entries.some(e => e.type === 'noun')).forEach(word => {
        const entry = word.entries.find(e => e.type === 'noun');
        const primaryEn = entry.senses[0].en[0];
        
        // Settings for the caser
        const numbers = ["singular", "dual", "paucal", "plural"];
        const cases = ["NOM", "ACC", "ERG", "DAT", "GEN", "LOC", "TRA", "ALL", "ABL", "INS", "COM"];
        
        // Add the root form first
        const rootForm = word.tv.toLowerCase();
        if (!lookup[rootForm]) {
            lookup[rootForm] = { en: primaryEn, type: 'noun', data: word, details: { case: "Root", number: "singular" } };
        }

        // Add all declined forms
        cases.forEach(c => {
            numbers.forEach(n => {
                // We use the global bridge functions (NOM, ACC, etc.)
                const generated = window[c](word, n);
                if (!generated) return;
                const form = generated.toLowerCase();
                
                if (form && form !== "n/a" && !lookup[form]) {
                    lookup[form] = { 
                        en: primaryEn, 
                        type: 'noun', 
                        data: word,
                        details: { case: c, number: n } 
                    };
                }
            });
        });
    });

    // 2. Map Verbs
    lexicon.filter(w => w.entries.some(e => e.type === 'verb')).forEach(word => {
        const entry = word.entries.find(e => e.type === 'verb');
        const primaryEn = entry.senses[0].en[0];

        const people = ["1st inclusive", "1st exclusive", "2nd formal", "2nd informal", "3rd animate", "3rd inanimate"];
        const numbers = ["singular", "dual", "paucal", "plural"];
        const tenses = ["present perfective", "past perfective", "future perfective"]; 

        people.forEach(p => {
            // Determine class from person string
            const nClass = p.includes("inanimate") ? "inanimate" : "animate";
            
            numbers.forEach(num => {
                tenses.forEach(t => {
                    // Use the V bridge function
                    // console.log("Processing word:", word.tv);
                    const generated = V(word, nClass, p, num, t);
                    if (!generated) return;
                    const form = generated.toLowerCase();
                    
                    if (form && !lookup[form]) {
                        lookup[form] = { 
                            en: primaryEn, 
                            type: 'verb', 
                            data: word,
                            details: { person: p, number: num, tense: t }
                        };
                    }
                });
            });
        });
        
        // Ensure the bare infinitive/stem is also in the lookup
        if (!lookup[word.tv.toLowerCase()]) {
            lookup[word.tv.toLowerCase()] = { en: primaryEn, type: 'verb', data: word, details: { tense: "infinitive" } };
        }
    });

    return lookup;
}
