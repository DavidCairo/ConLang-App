const findByEn = (enString) => {
    // 1. Safety check: If enString is null, undefined, or not a string, return null
    if (!enString || typeof enString !== 'string') return null;

    // 2. Perform the search with optional chaining to prevent "cannot read property of undefined" errors
    return lexicon.find(word => 
        word.entries?.some(entry => 
            entry.senses?.some(sense => 
                sense.en?.some(e => e.toLowerCase() === enString.toLowerCase())
            )
        )
    );
};


